#!/bin/bash
# ============================================================
# Author: wilbur
# Version: 2.1
#   Date: 2026-05-08
#   Description: 修复 Markdown 文档中的公式格式问题
#              1. 将 \[公式\] 改为 $$公式$$ (公式块)
#              2. 将 \(公式\) 改为 $公式$ (行内公式，非表格区域)
#              3. 表格内的公式保持 \(...\) 格式
# ============================================================

set -e

if [ -z "$1" ]; then
    echo "用法: $0 <markdown文件路径>"
    exit 1
fi

FILE="$1"

if [ ! -f "$FILE" ]; then
    echo "错误: 文件 '$FILE' 不存在"
    exit 1
fi

# 备份原文件
cp "$FILE" "${FILE}.bak"
echo "已备份原文件到: ${FILE}.bak"

# 修复问题1: \[公式\] -> $$公式$$
sed -i '' 's/\\\[/\$\$/g' "$FILE"
sed -i '' 's/\\\]/\$\$/g' "$FILE"

# 使用 perl 处理表格内外的不同公式格式
# 思路：在 <table> 标签内，保持 \( 和 \) 不变
# 在 </table> 标签外，把 \( 改成 $，\) 改成 $
perl -i -pe '
    if (/<table>/ .. /<\/table>/) {
        # 在表格内，不转换 \( 和 \)
        # 但需要处理 [公式] 的情况（已经由上面的 sed 处理）
    } else {
        # 非表格区域：\( 改成 $，\) 改成 $
        s/\\(/\$\$/g;
        s/\\)/\$\$/g;
    }
' "$FILE"

echo "修复完成！"
echo ""
echo "修改内容:"
echo "  1. \[公式\] -> $$公式$$ (公式块)"
echo "  2. 非表格区域的 \(公式\) -> $公式$ (行内公式)"
echo "  3. 表格内保持 \(公式\) 格式不变"
